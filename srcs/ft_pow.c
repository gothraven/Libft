#include "libft.h"

int		ft_pow(int x, unsigned int y)
{
	if (y == 0)
		return (1);
	else if (y % 2 == 0)
		return (ft_pow(x, y / 2) * ft_pow(x, y / 2));
	else
		return (x * ft_pow(x, y / 2) * ft_pow(x, y / 2));
}
