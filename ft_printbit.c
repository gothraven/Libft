#include "libft.h"

void	ft_printbit(int val)
{
	int		i;
	char	s[32];

	i = 0;
	while (i < 32)
	{
		if (val & 2147483648)
			s[i] = '1';
		else
			s[i] = '0';
		i++;
		val = val << 1;
	}
	s[i] = '\0';
	write(1, s, 32);
}
